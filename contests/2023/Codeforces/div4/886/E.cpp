#define F first
#define S second
#define SIZE(c) int((c).size())
#define ALL(c) begin(c), end(c)

typedef long long   ll;
typedef long double ld;

const char nl = '\n';

void solve() {
    ll n;
    ld c;
    cin >> n;
    cin >> c;

    ld S[n];
    for(auto& s: S) cin >> s;

    ld b = 0, a = 0, acc = 0;
    for(auto& s: S) {
        a += 4;
        b += 4*s;
        acc += s*s;
    }

    // a*w2+bw+acc=c;
    // a*w2+bw+(acc-c)=0;

    ld w = (-1*b + sqrt(b*b - 4*a*(acc-c)))/(2*a);
    cout << (ll)w << nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t;
    cin >> t;

    while(t--)
        solve();

    return 0;
}
