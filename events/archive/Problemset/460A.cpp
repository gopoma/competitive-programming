const char nl = '\n';

void solve() {
    int n, m;
    cin >> n >> m;

    int r = n; // 32
    // 32/3->10
    // ((32%3)+(32/3))/3=(2+10)/3=4
    // 4/3->1
    // ((4%3)+(4/3))/3 -> (1+1)/3=0
    // 0/3

    while(n/m) {
        r += n/m;
        n = (n%m)+(n/m);
    }

    cout << r << nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t = 1;
    // cin >> t;

    while(t--)
        solve();

    return 0;
}
