constexpr i64 P = 998244353LL;
using Z = MLong<P>;

void solve() {
    int n;
    cin >> n;

    vector<pair<long long, long long>> a(n);
    for(int i = 1; i <= n; i++) {
        long long e;
        cin >> e;

        a.emplace_back(make_pair(e, i));
    }

    sort(a.rbegin(), a.rend());

    Z ans = 0;
    Z count = power(Z(2), n - 1);
    set<long long> already;
    for(int i = 0; i < n; i++) {
        vector<long long> divisors = getDivisors(a[i].second);
        for(auto& d: divisors) {
            if(already.find(d) == already.end()) {
                already.insert(d);

                ans += a[i].first * count;

                // process
                count /= 2;
            }
        }
    }

    cout << ans << n_l;
}

