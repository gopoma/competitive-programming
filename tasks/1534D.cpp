void slv(ll n, vpl Edges, bool dev) {
    ll amount_queries = 0;
    auto query = [&](ll src) -> vl {
        amount_queries++;
        if(amount_queries > cdiv(n, 2LL)) {
            dbg("Exceeded", amount_queries);
            chk(false);
        }

        if(dev) {
            vvl adj(n);
            for(auto& [u, v]: Edges) {
                adj[u].eb(v);
                adj[v].eb(u);
            }
            vl dist(n);
            auto dfs = [&](const auto& dfs, int u, int parent, int depth) -> void {
                dist[u] = depth;
                for(auto& nxt: adj[u]) if(nxt != parent) {
                    dfs(dfs, nxt, u, depth + 1);
                }
            }; dfs(dfs, src, -1, 0);
            return dist;
        } else {
            cout << "? " << (src + 1) << endl;
            vl dist(n);
            for(auto& x: dist) cin >> x;
            return dist;
        }
    };

    vpl edges;
    vb vis(n);
    auto addEdge = [&](ll u, ll v) -> void {
        vis[u] = vis[v] = true;
        edges.eb(min(u, v), max(u, v));
    };

    vl odd, even;
    {
        vl dist = query(0);
        for(int u = 0; u < n; u++) {
            if(u == 0) continue;
            if(dist[u] == 1) addEdge(0, u);
            if(dist[u] & 1) odd.eb(u);
            else even.eb(u);
        }
    }

    // dbg(edges);
    // dbg(odd, even);

    vl nodes;
    if(sz(odd) < sz(even)) nodes = odd;
    else nodes = even;

    // dbg(nodes);

    for(auto& u: nodes) {
        vl dist = query(u);
        for(int v = 0; v < n; v++) {
            if(dist[v] == 1) {
                addEdge(u, v);
            }
        }
        // dbg(u, edges);
    }

    remDup(edges);
    // dbg(edges, Edges);
    chk(edges == Edges);
    cout << "!\n";
    for(auto& [u, v]: edges) cout << (u + 1) << " " << (v + 1) << endl;
}

