const int INF = int(1e9);
void solve() {
    //? <>
    str S; cin >> S;
    const int N = sz(S);
    dbg(N);
    dbg(S);

    vi Z = z(S);
    const int MAXN = *max_element(all(Z));

    BIT<int> st; st.init(MAXN + 5);
    for(int i = 1; i < N; i++) st.add(Z[i], +1);

    int mx = -INF;
    for(int i = N - 1; i >= 1; i--) {
        int L = (N - 1) - i + 1;
        st.add(Z[i], -1);
        if(Z[i] == L && st.sum(L, MAXN - 1)) {
            ckmax(mx, L);
        }
    }
    if(mx != -INF) cout << S.substr(0, mx) << "\n";
    else cout << "Just a legend\n";
}

