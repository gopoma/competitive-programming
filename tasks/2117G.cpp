using Edge = tuple<ll, ll, ll>;
const ll BIG = ll(1e18);
void solve() {
    // <>
    ll n, m; cin >> n >> m;

    vector<Edge> E(m);
    for(auto& [w, u, v]: E) {
        cin >> u >> v >> w;
        u--; v--;
    }

    sort(all(E));

    DSU dsu; dsu.init(n);
    ll res = BIG;
    for(auto& [w, u, v]: E) {
        dbg(w, u, v);

        int mn_u = dsu.mn[dsu.get(u)];
        int mx_u = dsu.mx[dsu.get(u)];

        int mn_v = dsu.mn[dsu.get(v)];
        int mx_v = dsu.mx[dsu.get(v)];

        dsu.unite(u, v);

        dsu.mn[dsu.get(u)] = min({dsu.mn[dsu.get(u)], mn_u, mn_v, int(w)});
        dsu.mx[dsu.get(u)] = max({dsu.mx[dsu.get(u)], mx_u, mx_v, int(w)});

        if(dsu.sameSet(0, n - 1)) {
            res = min(res, ll(dsu.mn[dsu.get(0)] + dsu.mx[dsu.get(0)]));
        }
    }
    cout << res << "\n";
}

