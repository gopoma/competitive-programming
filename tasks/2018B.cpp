void solve() {
    ll n; cin >> n;
    vector<ll> a(n); for(auto& x: a) cin >> x;
    dbg(n, a);

    vector<ll> b(n), c(n);
    for(int i = 0; i < n; i++) {
        b[i] = a[i] - (i + 1);
        c[i] = a[i] - (n - i);
    }

    SegTree<ll> stf, stb; stf.init(n); stb.init(n);
    for(int i = 0; i < n; i++) {
        stf.upd(i, b[i]);
        stb.upd(i, c[i]);
    }

    int L = 0;
    int R = n - 1;
    int need = n;
    int re = 0;
    while(L <= R) {
        if(a[L] >= need) {
            ll mn = stf.query(L, R);
            mn += (L);

            if(mn >= 0) re++;

            L++;
            need--;
            continue;
        }
        if(a[R] >= need) {
            ll mn = stb.query(L, R);
            mn += ((n - 1) - R);

            if(mn >= 0) re++;

            R--;
            need--;
            continue;
        }
        break;
    }
    cout << re << "\n";
}

