using Info = tuple<int, int, int>;
vpi slv(int n, vi a) {
    V<Info> edges;
    for(int i = 0; i < n; i++) {
        for(int mod = 1; mod <= n - 1; mod++) {
            edges.eb(-mod, a[i] % mod, i);
        }
    }
    sor(edges);
    // dbg(edges);

    DSU dsu; dsu.init(n);

    vpi response;
    int currentMod = n - 1;
    for(int i = 0; i + 1 < sz(edges); i++) {
        auto [mod, remain, node] = edges[i];
        mod = -mod;
        auto [nxtMod, nxtRemain, nxtNode] = edges[i + 1];
        nxtMod = -nxtMod;
        if(mod == currentMod && mod == nxtMod && remain == nxtRemain && !dsu.sameSet(node, nxtNode)) {
            currentMod--;
            dsu.unite(node, nxtNode);
            response.eb(node, nxtNode);
        }
    }

    // {
    //     set<int> U;
    //     for(int i = 0; i < n; i++) U.emplace(dsu.get(i));
    //     chk(sz(U) == 1);
    // }

    reverse(all(response));
    return response;
}

