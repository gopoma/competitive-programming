//* Template
ll cdiv(ll a, ll b) {
	return a / b + ((a ^ b) > 0 && a % b);
}  // divide a by b rounded up
ll fdiv(ll a, ll b) {
	return a / b - ((a ^ b) < 0 && a % b);
}  // divide a by b rounded down

//* /Template

void solve() {
    ll n; cin >> n;
    vl a(n); for(auto& x: a) cin >> x;

    ll res = ll(1e18);

    {
        vl b = a;
        sort(b.begin(), b.end());
        res = min(res, cdiv(b[0], 2LL) + cdiv(b[1], 2LL));
    } dbg(res);

    for(int i = 0; i + 1 < n; i++) {
        auto check = [&](ll middle) -> bool {
            ll x = a[i];
            ll y = a[i + 1];

            x -= middle;
            y -= middle;
            x = max(0LL, x);
            y = max(0LL, y);

            ll extra = x + y;
            bool ok = extra <= middle;
            return ok;
        };

        ll left = 0; // always bad
        ll right = ll(1e9); // always good

        while(left + 1 < right) {
            ll middle = (left + right) >> 1LL;

            if(check(middle)) right = middle;
            else left = middle;
        }

        res = min(res, right);
    } dbg(res);

    for(int i = 0; i + 2 < n; i++) {
        ll x = a[i];
        ll y = a[i + 2];


        ll mn = min(x, y);
        ll taken = mn;
        x -= mn;
        y -= mn;

        taken += cdiv(x, 2LL) + cdiv(y, 2LL);

        res = min(res, taken);
    } dbg(res);

    cout << res << "\n";
}

