void slv(vP points) {
    vP guardaPoints = points;
    // dbg(guardaPoints);

    auto dist = [&](P p, P p2) {
        return abs(p.f - p2.f) + abs(p.s - p2.s);
    };

    sor(points);

    vl xx, yy;
    for(auto& [x, y]: points) {
        xx.eb(x);
        yy.eb(y);
    }
    remDup(xx);
    remDup(yy);

    vP newPoints;
    for(auto& x: xx) {
        for(auto& y: yy) {
            newPoints.eb(x, y);
        }
    }
    remDup(newPoints);
    points = newPoints;

    map<P, int> forwards;
    for(int i = 0; i < sz(points); i++) forwards[points[i]] = i;

    vpi edges;
    for(int i = 0; i < sz(points); i++) {
        for(int j = i + 1; j < sz(points); j++) {
            auto [x, y]   = points[i];
            auto [x2, y2] = points[j];

            if(x == x2) edges.eb(i, j);
            if(y == y2) edges.eb(i, j);
        }
    }
    remDup(edges);

    for(auto& [u, v]: edges) dbg(points[u], points[v]);

    const int M = sz(edges);
    dbg(M);

    using Info = pair<ll, V<Line>>;
    Info ans = mp(BIG, V<Line>());
    for(int mask = 0; mask < (1 << M); mask++) {
        DSU dsu; dsu.init(sz(points));

        ll d = 0;
        V<Line> lines;
        for(int i = 0; i < M; i++) {
            if(mask & (1 << i)) {
                auto [u, v] = edges[i];

                dsu.unite(u, v);

                d += dist(points[u], points[v]);
                lines.eb(points[u], points[v]);
            }
        }

        bool ok = true;
        int target = dsu.get(forwards[guardaPoints.ft]);
        for(auto& it: guardaPoints) ok &= (target == dsu.get(forwards[it]));

        if(ok && ckmin(ans, mp(d, lines))) {}
    }

    dbg(ans);
    cout << sz(ans.s) << "\n";
    for(auto& [p, p2]: ans.s) {
        auto [x, y] = p;
        auto [x2, y2] = p2;
        cout << x << " " << y << " " << x2 << " " << y2 << "\n";
    }
}

